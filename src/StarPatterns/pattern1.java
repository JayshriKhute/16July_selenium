package StarPatterns;

public class pattern1
{
public static void main(String[] args) 
{
	System.out.println("Printing vertical stars:");
	for(int i=1;i<=5;i++)
	{
		System.out.println("*");
	}
	
	System.out.println();
	System.out.println();
	System.out.println("Printing Horizontal stars");
	for(int i=1;i<=5;i++)
	{
		System.out.print("*");
	}

}
}
